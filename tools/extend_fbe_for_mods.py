# Source aliases. Some mods name their internal `__name__` differently
# from the mod-list entry. The canonical name is the one inside the
# `data/` tree on disk and inside mod zip filenames.
_SOURCE_ALIASES = {
    # No known aliases yet. Add here if a mod ships __foo__ in its
    # prototype paths but ships its zip as ``Foo``.
}


@dataclass
class SourceMap:
    """Where to look for a sprite source named ``__<key>__``.

    ``disk_root`` is a directory we copy from byte-for-byte
    (game DLC). ``zip_path`` is a mod .zip we extract from
    (``<zip_path>``::<mod_name>/<rel> by Factorio's convention).
    Either ``disk_root`` or ``zip_path`` may be set, never both.
    """

    key: str
    disk_root: Path | None = None
    zip_path: Path | None = None
    zip_inner_prefix: str | None = None  # e.g. "AdjustableModule_1.1.3"


def discover_sources(enabled_mods: list[str]) -> dict[str, SourceMap]:
    """Build a map ``__source__`` -> SourceMap for everything we can reach.

    Always covers ``__base__``, ``__core__``. Then every game DLC
    directory present in the install. Then every enabled mod whose zip
    we can find under ``~/.factorio/mods/``.
    """
    out: dict[str, SourceMap] = {}

    game = find_game_data_root()
    if game is not None:
        for sub in game.iterdir():
            if sub.is_dir():
                out[sub.name] = SourceMap(key=sub.name, disk_root=sub)
                # Also expose under aliased name.
                for alias, real in _SOURCE_ALIASES.items():
                    if real == sub.name:
                        out[alias] = SourceMap(key=sub.name, disk_root=sub)

    # Mods.
    for name in enabled_mods:
        if name in {"base", "core", "space-age", "quality", "elevated-rails"}:
            continue
        zp, prefix = _find_mod_zip(name)
        if zp is None:
            continue
        out[name] = SourceMap(key=name, zip_path=zp, zip_inner_prefix=prefix)

    return out


def _find_mod_zip(mod_name: str) -> tuple[Path | None, str | None]:
    """Find the highest-version zip for ``mod_name`` under MODS_DIR.

    Returns (zip_path, inner_prefix). The inner prefix is the top-level
    directory inside the zip, which Factorio mandates be
    ``<mod_name>_<version>`` (or sometimes just ``<mod_name>``).
    """
    if not MODS_DIR.is_dir():
        return None, None
    candidates: list[tuple[tuple[int, ...], Path]] = []
    for p in MODS_DIR.iterdir():
        if not p.suffix == ".zip":
            continue
        stem = p.stem  # e.g. AdjustableModule_1.1.3
        if not (stem == mod_name or stem.startswith(mod_name + "_")):
            continue
        ver_part = stem[len(mod_name) + 1:] if "_" in stem and stem != mod_name else "0"
        try:
            ver = tuple(int(x) for x in ver_part.split("."))
        except ValueError:
            ver = (0,)
        candidates.append((ver, p))
    if not candidates:
        return None, None
    candidates.sort()
    zp = candidates[-1][1]
    # Probe inner prefix.
    try:
        with zipfile.ZipFile(zp) as zf:
            top_dirs = {n.split("/", 1)[0] for n in zf.namelist() if "/" in n}
    except zipfile.BadZipFile:
        return zp, None
    if mod_name in top_dirs:
        return zp, mod_name
    # Look for <mod_name>_<version> form.
    for d in top_dirs:
        if d.startswith(mod_name + "_") or d == mod_name:
            return zp, d
    if len(top_dirs) == 1:
        return zp, next(iter(top_dirs))
    return zp, None


def resolve_sprite_bytes(ref: str, sources: dict[str, SourceMap]) -> bytes | None:
    """Return the raw bytes for a sprite reference like ``__source__/path.png``.

    Returns None if we cannot find it.
    """
    m = re.match(r"__([A-Za-z0-9_\-]+)__/(.+)$", ref)
    if not m:
        return None
    source, rel = m.group(1), m.group(2)

    sm = sources.get(source)
    if sm is None and source in _SOURCE_ALIASES:
        sm = sources.get(_SOURCE_ALIASES[source])
    if sm is None:
        return None

    if sm.disk_root is not None:
        candidate = sm.disk_root / rel
        if candidate.is_file():
            try:
                return candidate.read_bytes()
            except OSError:
                return None
        return None
    if sm.zip_path is not None:
        # Try a few inner-prefix variants.
        prefixes = [sm.zip_inner_prefix or sm.key, sm.key]
        try:
            with zipfile.ZipFile(sm.zip_path) as zf:
                names = set(zf.namelist())
                for prefix in prefixes:
                    if not prefix:
                        continue
                    candidate = f"{prefix}/{rel}"
                    if candidate in names:
                        return zf.read(candidate)
        except zipfile.BadZipFile:
            return None
    return None


