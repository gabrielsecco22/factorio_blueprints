@dataclass
class ExtendStats:
    entities_added: list[str] = field(default_factory=list)
    entities_overlaid: list[tuple[str, dict[str, Any]]] = field(default_factory=list)
    items_added: int = 0
    recipes_added: int = 0
    fluids_added: int = 0
    sprites_copied: int = 0
    sprites_exists: int = 0
    sprites_missing: int = 0
    sprites_missing_files: list[str] = field(default_factory=list)
    bundle_files_patched: int = 0
    sources_used: list[str] = field(default_factory=list)


def overlay_diff(fbe_entry: dict[str, Any], dump_entry: dict[str, Any]) -> dict[str, Any]:
    """Return whitelist-driven diff dump->fbe for OVERLAY_FIELDS_ENTITY."""
    out: dict[str, Any] = {}
    for f in OVERLAY_FIELDS_ENTITY:
        if f in dump_entry and dump_entry[f] != fbe_entry.get(f):
            out[f] = {"old": fbe_entry.get(f), "new": dump_entry[f]}
    return out


def extend(dump: dict[str, Any], fbe: dict[str, Any], sources: dict[str, SourceMap],
           dry_run: bool, force: bool) -> ExtendStats:
    stats = ExtendStats()
    stats.sources_used = sorted(sources.keys())

    fbe_entities = fbe.setdefault("entities", {})
    fbe_items = fbe.setdefault("items", {})
    fbe_recipes = fbe.setdefault("recipes", {})
    fbe_fluids = fbe.setdefault("fluids", {})

    dump_entities = all_entities_in_dump(dump)
    dump_items = all_items_in_dump(dump)
    dump_recipes = all_recipes_in_dump(dump)
    dump_fluids = all_fluids_in_dump(dump)

    # Sprites we need to copy. Collected as we go.
    sprite_refs: set[str] = set()

    # 1. Entities.
    for name, dentry in dump_entities.items():
        if name in fbe_entities:
            # Overlay modded values onto existing FBE entry. We keep
            # FBE's vanilla sprite/circuit-connector geometry untouched
            # because re-deriving those from the dump can desync against
            # the visible PixiJS atlas slicing.
            diff = overlay_diff(fbe_entities[name], dentry)
            if diff:
                if not dry_run:
                    for f in diff:
                        fbe_entities[name][f] = dentry[f]
                stats.entities_overlaid.append((name, diff))
            continue
        # Brand-new entity: copy wholesale.
        if not dry_run:
            fbe_entities[name] = dentry
        stats.entities_added.append(name)
        sprite_refs.update(collect_png_refs(dentry))

    # 2. Items.
    for name, dentry in dump_items.items():
        if name in fbe_items:
            continue
        if not dry_run:
            fbe_items[name] = dentry
        stats.items_added += 1
        sprite_refs.update(collect_png_refs(dentry))

    # 3. Recipes.
    for name, dentry in dump_recipes.items():
        if name in fbe_recipes:
            continue
        if not dry_run:
            fbe_recipes[name] = dentry
        stats.recipes_added += 1
        sprite_refs.update(collect_png_refs(dentry))

    # 4. Fluids.
    for name, dentry in dump_fluids.items():
        if name in fbe_fluids:
            continue
        if not dry_run:
            fbe_fluids[name] = dentry
        stats.fluids_added += 1
        sprite_refs.update(collect_png_refs(dentry))

    # 5. Copy sprites.
    for ref in sorted(sprite_refs):
        kind = copy_sprite(ref, sources, dry_run)
        if kind == "copied":
            stats.sprites_copied += 1
        elif kind == "exists":
            stats.sprites_exists += 1
        elif kind in ("missing-file", "missing-source"):
            stats.sprites_missing += 1
            if len(stats.sprites_missing_files) < 50:
                stats.sprites_missing_files.append(ref)

    # 6. Sentinel. Reflects the patched-vs-upstream delta as observed in
    #    THIS run -- on the second run nothing should change so the
    #    sentinel is the authoritative final state. We always re-derive
    #    the totals by counting against data.json.upstream so the values
    #    are stable across re-runs (idempotent in the strict sense).
    if not dry_run:
        upstream_counts = _upstream_counts()
        cur_entities = len(fbe_entities)
        cur_items = len(fbe_items)
        cur_recipes = len(fbe_recipes)
        cur_fluids = len(fbe_fluids)
        # Overlaid counts from this run (overlays don't accumulate; we
        # just persist the count from the most recent diff).
        # If nothing changed (re-run), the previous overlaid count is
        # still authoritative -- preserve it.
        existing = fbe.get(SENTINEL_KEY) or {}
        overlaid_count = (
            len(stats.entities_overlaid)
            if stats.entities_overlaid
            else int(existing.get("entities_overlaid") or 0)
        )
        sprites_copied_total = stats.sprites_copied + stats.sprites_exists
        fbe[SENTINEL_KEY] = {
            "version": 3,
            "entities_added": max(0, cur_entities - upstream_counts.get("entities", 0)),
            "entities_overlaid": overlaid_count,
            "items_added": max(0, cur_items - upstream_counts.get("items", 0)),
            "recipes_added": max(0, cur_recipes - upstream_counts.get("recipes", 0)),
            "fluids_added": max(0, cur_fluids - upstream_counts.get("fluids", 0)),
            "sprites_copied": sprites_copied_total,
            "sprites_missing": stats.sprites_missing,
            "sources": sorted(sources.keys()),
        }

    return stats


def _upstream_counts() -> dict[str, int]:
    """Return prototype counts in the unmodified upstream data.json,
    so the sentinel can report `entities_added = current - upstream`.

    Falls back to baseline numbers (FBE 2025-10 build) if the backup
    is missing.
    """
    if FBE_DATA_UPSTREAM.is_file():
        try:
            up = json.loads(FBE_DATA_UPSTREAM.read_text())
            return {
                "entities": len(up.get("entities") or {}),
                "items": len(up.get("items") or {}),
                "recipes": len(up.get("recipes") or {}),
                "fluids": len(up.get("fluids") or {}),
            }
        except (OSError, json.JSONDecodeError):
            pass
    return {"entities": 121, "items": 251, "recipes": 214, "fluids": 19}


