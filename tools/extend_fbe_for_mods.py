#!/usr/bin/env python3
"""Extend FBE's vendored bundle with mod-aware data + sprites.

Background
----------

The Factorio Blueprint Editor (FBE) bundle vendored under
``studio/static/fbe/`` ships only ``__base__`` + ``__core__`` prototypes
(121 entities, 214 recipes, 19 fluids). It cannot render Space Age
entities (foundry, electromagnetic-plant, biochamber, recycler,
cryogenic-plant, agricultural-tower, asteroid-collector, etc.) and it
applies vanilla parameter values for entities the user has tweaked
through mods (e.g. AdjustableModule increases beacon module_slots from
2 to 5).

This tool patches FBE's ``data.json`` and copies the corresponding
PNG sprites out of the local Factorio install so the iframe preview
renders the user's actual mod set.

What it does
------------

1. Loads ``specs/data-raw-dump.json`` (the authoritative live dump from
   the user's exact enabled mod set, generated by ``factorio
   --dump-data``) and ``~/.factorio/mods/mod-list.json``.
2. Diffs the dump against ``studio/static/fbe/data/data.json`` to find
   entities/items/recipes/fluids that are missing from FBE.
3. For each missing prototype, copies the dump entry verbatim into
   ``data.json``. The dump entries are already in FBE-compatible shape
   (same key names, same filename schema with ``__base__/...`` prefixes).
4. For entities that already exist in FBE but have different values in
   the dump (modded ``module_slots``, ``allowed_effects``,
   ``distribution_effectivity``, etc.) overlays the modded values onto
   the FBE entry. The overlay is whitelist-driven so we don't disturb
   sprite/circuit-connector geometry.
5. Walks every newly-added or overlaid prototype to collect referenced
   PNG paths (``__space-age__/graphics/icons/foundry.png``, etc.) and
   copies the matching files from the local Factorio install
   (``~/.local/share/Steam/.../Factorio/data/<source>/``) or extracts
   them from mod zips at ``~/.factorio/mods/<mod>.zip``.
6. Patches FBE's bundled JS to load ``.png`` files directly instead of
   transcoding ``.basis`` (the upstream cloud bundle uses Basis Universal
   for compactness; we don't have ``basisu`` locally so we ship raw PNGs).
7. Writes a backup of the original ``data.json`` to ``data.json.upstream``
   on first run so the change is fully reversible.
8. Idempotent: re-running detects what's already done by checking a
   ``__factudio_extension__`` sentinel in the patched ``data.json``.

CLI
---

    python3 tools/extend_fbe_for_mods.py            # apply
    python3 tools/extend_fbe_for_mods.py --dry-run  # report only
    python3 tools/extend_fbe_for_mods.py --force    # reapply from upstream

The exit code is 0 on success, non-zero if any required input is
missing (no dump, no game install, no FBE bundle).
"""

