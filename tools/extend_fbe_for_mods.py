def is_renderable_entity(entry: dict[str, Any]) -> bool:
    t = entry.get("type")
    if t not in RENDERABLE_TYPES:
        return False
    flags = entry.get("flags") or []
    if "hidden" in flags or "not-blueprintable" in flags:
        # Still keep these for completeness; they may show up in saved
        # blueprints from other users. Just skip "hidden" specifically
        # if also lacks placement.
        if not (
            "placeable-neutral" in flags
            or "placeable-player" in flags
            or "player-creation" in flags
        ):
            return False
    return True


def all_entities_in_dump(dump: dict[str, Any]) -> dict[str, dict[str, Any]]:
    """Flatten every entity-like prototype in the dump, keyed by name.

    If two categories share a name (rare), the first wins.
    """
    out: dict[str, dict[str, Any]] = {}
    for cat, items in dump.items():
        if not isinstance(items, dict):
            continue
        if not items:
            continue
        sample = next(iter(items.values()))
        if not isinstance(sample, dict):
            continue
        sample_type = sample.get("type")
        if sample_type != cat:
            continue
        if cat not in RENDERABLE_TYPES:
            continue
        for n, e in items.items():
            if not isinstance(e, dict):
                continue
            if not is_renderable_entity(e):
                continue
            if n in out:
                continue
            out[n] = e
    return out


def all_items_in_dump(dump: dict[str, Any]) -> dict[str, dict[str, Any]]:
    out: dict[str, dict[str, Any]] = {}
    for cat in ITEM_LIKE:
        if cat in dump and isinstance(dump[cat], dict):
            for n, e in dump[cat].items():
                if isinstance(e, dict) and n not in out:
                    out[n] = e
    return out


def all_recipes_in_dump(dump: dict[str, Any]) -> dict[str, dict[str, Any]]:
    return {n: e for n, e in (dump.get("recipe") or {}).items() if isinstance(e, dict)}


def all_fluids_in_dump(dump: dict[str, Any]) -> dict[str, dict[str, Any]]:
    return {n: e for n, e in (dump.get("fluid") or {}).items() if isinstance(e, dict)}


# --------------------------------------------------------------------------
# Bundle PNG-load patch.
# --------------------------------------------------------------------------

# The upstream FBE bundle does
#     `/fbe/data/${i.replace(".png",".basis")}`
# which assumes sprite atlases were pre-converted to Basis Universal.
# We don't have basisu, so we ship raw PNGs and patch the bundle to load
# `.png` directly. We replace the substring with one of the same length so
# byte offsets and sourcemaps stay valid.
_BUNDLE_PATCH_OLD = '.replace(".png",".basis")'
_BUNDLE_PATCH_NEW = '.replace(".png",".png"  )'  # same length; no-op
assert len(_BUNDLE_PATCH_OLD) == len(_BUNDLE_PATCH_NEW), "patch must preserve byte length"


def patch_fbe_bundle(dry_run: bool) -> tuple[int, list[Path]]:
    """Patch every JS asset under FBE_ASSETS_DIR. Returns (count, files)."""
    if not FBE_ASSETS_DIR.is_dir():
        return 0, []
    patched = 0
    touched: list[Path] = []
    for p in FBE_ASSETS_DIR.glob("*.js"):
        try:
            text = p.read_text(encoding="utf-8")
        except OSError:
            continue
        if _BUNDLE_PATCH_OLD not in text:
            continue
        new_text = text.replace(_BUNDLE_PATCH_OLD, _BUNDLE_PATCH_NEW)
        if dry_run:
            patched += 1
            touched.append(p)
            continue
        # Back up exactly once per file.
        backup = p.with_suffix(p.suffix + ".upstream")
        if not backup.exists():
            backup.write_text(text, encoding="utf-8")
        p.write_text(new_text, encoding="utf-8")
        patched += 1
        touched.append(p)
    return patched, touched


# --------------------------------------------------------------------------
# Sprite copy.
# --------------------------------------------------------------------------


def copy_sprite(ref: str, sources: dict[str, SourceMap], dry_run: bool) -> str:
    """Copy ``__source__/path.png`` to FBE_DIR/data/__source__/path.png.

    Returns one of: ``"copied"``, ``"exists"``, ``"missing-source"``,
    ``"missing-file"``, ``"skipped"``.
    """
    m = re.match(r"__([A-Za-z0-9_\-]+)__/(.+)$", ref)
    if not m:
        return "skipped"
    source_dir = f"__{m.group(1)}__"
    rel = m.group(2)
    dest = FBE_DATA_DIR / source_dir / rel
    if dest.is_file() and dest.stat().st_size > 0:
        return "exists"
    data = resolve_sprite_bytes(ref, sources)
    if data is None:
        if m.group(1) not in sources and m.group(1) not in _SOURCE_ALIASES:
            return "missing-source"
        return "missing-file"
    if dry_run:
        return "copied"
    dest.parent.mkdir(parents=True, exist_ok=True)
    tmp = dest.with_suffix(dest.suffix + ".part")
    tmp.write_bytes(data)
    tmp.replace(dest)
    return "copied"


