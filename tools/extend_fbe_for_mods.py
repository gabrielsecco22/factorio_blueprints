_REF_RE = re.compile(r"__[A-Za-z0-9_\-]+__/[^\"\\]+\.png")


def collect_png_refs(obj: Any) -> set[str]:
    """Return all ``__source__/path.png`` strings reachable from ``obj``."""
    blob = json.dumps(obj, separators=(",", ":"), default=str)
    return set(_REF_RE.findall(blob))


