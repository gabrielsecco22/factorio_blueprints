def load_enabled_mods() -> list[str]:
    """Return the list of mods enabled in mod-list.json (in declaration order).

    Returns an empty list (with a warning) if the file is missing.
    """
    if not MOD_LIST.is_file():
        log(f"warning: {MOD_LIST} not found; no mod overlay will be applied")
        return []
    try:
        data = json.loads(MOD_LIST.read_text())
    except json.JSONDecodeError as e:
        err(f"could not parse {MOD_LIST}: {e}")
        return []
    out: list[str] = []
    for m in data.get("mods", []):
        if m.get("enabled"):
            out.append(m["name"])
    return out


def find_game_data_root() -> Path | None:
    for c in GAME_DATA_CANDIDATES:
        if c.is_dir() and (c / "base").is_dir():
            return c
    return None


