def already_extended(fbe: dict[str, Any]) -> bool:
    return SENTINEL_KEY in fbe


def load_fbe_data(force: bool) -> dict[str, Any]:
    """Load FBE data.json, restoring from the upstream backup if --force."""
    if force and FBE_DATA_UPSTREAM.is_file():
        log("--force: restoring data.json from data.json.upstream")
        shutil.copy2(FBE_DATA_UPSTREAM, FBE_DATA)
    return json.loads(FBE_DATA.read_text())


def write_fbe_data(fbe: dict[str, Any]) -> None:
    """Write data.json, preserving the existing upstream backup if any."""
    if not FBE_DATA_UPSTREAM.is_file():
        log(f"backing up upstream data.json to {FBE_DATA_UPSTREAM.relative_to(REPO_ROOT)}")
        shutil.copy2(FBE_DATA, FBE_DATA_UPSTREAM)
    tmp = FBE_DATA.with_suffix(FBE_DATA.suffix + ".part")
    tmp.write_text(json.dumps(fbe, separators=(",", ":")))
    tmp.replace(FBE_DATA)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="extend_fbe_for_mods",
        description="Extend FBE's bundled data.json with mod-aware entries + sprites.",
    )
    parser.add_argument("--dry-run", action="store_true",
                        help="don't write anything; just report what would change")
    parser.add_argument("--force", action="store_true",
                        help="reset data.json to the upstream backup before extending")
    parser.add_argument("--quiet", action="store_true",
                        help="reduce log spam (still prints the final summary)")
    args = parser.parse_args(argv)

    # Sanity checks.
    if not FBE_DATA.is_file():
        err(f"missing FBE bundle at {FBE_DATA}.\n"
            "Run `bash studio/setup_fbe.sh` first.")
        return 2
    if not DUMP_PATH.is_file():
        err(f"missing data dump at {DUMP_PATH}.\n"
            "Run `bash tools/dump_prototypes.sh` first.")
        return 2

    enabled_mods = load_enabled_mods()
    if not args.quiet:
        log(f"enabled mods: {len(enabled_mods)}")
    sources = discover_sources(enabled_mods)
    if not args.quiet:
        log(f"sprite sources discovered: {len(sources)}")
    game = find_game_data_root()
    if game is None:
        log("warning: no Factorio game install found; sprites for "
            "DLC entities (foundry, recycler, ...) will be missing")
    elif not args.quiet:
        log(f"game install: {game}")

    fbe = load_fbe_data(force=args.force)
    if already_extended(fbe) and not args.force:
        log("data.json already extended (sentinel present). Re-running to "
            "pick up any new prototypes; pass --force to reset to upstream.")

    log(f"loading dump: {DUMP_PATH}")
    dump = json.loads(DUMP_PATH.read_text())

    stats = extend(dump, fbe, sources, dry_run=args.dry_run, force=args.force)

    # Always patch the bundle (idempotent: already-patched bundles are
    # detected by the absence of the old substring).
    bundle_patched, touched = patch_fbe_bundle(dry_run=args.dry_run)
    stats.bundle_files_patched = bundle_patched
    if not args.quiet:
        for p in touched:
            log(f"patched bundle: {p.relative_to(FBE_DIR)}")

    if not args.dry_run:
        write_fbe_data(fbe)

    # Summary.
    print()
    print("Extension summary")
    print("-" * 60)
    print(f"  entities added:    {len(stats.entities_added)}")
    print(f"  entities overlaid: {len(stats.entities_overlaid)}")
    print(f"  items added:       {stats.items_added}")
    print(f"  recipes added:     {stats.recipes_added}")
    print(f"  fluids added:      {stats.fluids_added}")
    print(f"  sprites copied:    {stats.sprites_copied}")
    print(f"  sprites already:   {stats.sprites_exists}")
    print(f"  sprites missing:   {stats.sprites_missing}")
    print(f"  bundle JS patched: {stats.bundle_files_patched}")
    if stats.entities_overlaid:
        print()
        print("  Notable overlays (modded parameter changes):")
        for name, diff in stats.entities_overlaid[:8]:
            for f, vals in diff.items():
                old = vals["old"]
                new = vals["new"]
                if isinstance(old, list) or isinstance(new, list):
                    continue  # too noisy
                if isinstance(old, dict) or isinstance(new, dict):
                    continue
                print(f"    {name}.{f}: {old} -> {new}")
    if stats.sprites_missing_files:
        print()
        print("  Missing sprites (first 10):")
        for ref in stats.sprites_missing_files[:10]:
            print(f"    {ref}")
    if args.dry_run:
        print()
        print("  (dry-run: no files written)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
